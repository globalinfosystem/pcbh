<p><h3><center> Constitution of State Information Commission, Haryana to <br>implement Right to Information Act, 2005.</h3></center></p>
<p></p>
<p>In order to ensure the effective implementation of Right to Information Act, 2005, the State Government on 31st October 2005 by notification in the
 Official Gazette, constituted a body known as the State Information Commission, Haryana to exercise the powers conferred on, and to perform the functions
 assigned to, it under this Act and also appointed Shri G.Madhavan, IAS (Retired) as the first Chief Information Commissioner of the State Information 
 Commission, Haryana. He was sworn in on 1st November 2005 and the State Information Commission came into existence from this day and by a coincidence 
 the State of Haryana also came into existence on this very day in the year 1966. Later on vide notification dated 29th October 2010, Mrs. Meenaxi Anand 
 Chaudhry, IAS (Retired) was appointed as State Chief Information Commissioner who worked upto 28.04.2011.there after Shri Naresh Gulati, IAS (Retired) was her appointedas SCIC. 
 Who worked upto 20.5.2016.Now Sh.Yashpal Singal IPS(Retd) has sworn in as SCIC on 28.4.2017(forenoon).At present the following State information Commissioners are working in 
 the Commission:</p>
